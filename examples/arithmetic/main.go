package main

import (
	"encoding/base64"
	"encoding/json"
	"fmt"

	"github.com/hyperproperties/gorrupt/examples/arithmetic/pkg"
)

func main() {
	var input pkg.AdditionInput
	if err := json.Unmarshal([]byte{0x7b, 0x22, 0x43, 0x61, 0x6c, 0x63, 0x75, 0x6c, 0x61, 0x74, 0x6f, 0x72, 0x22, 0x3a, 0x7b, 0x22, 0x4f, 0x66, 0x66, 0x73, 0x65, 0x74, 0x22, 0x3a, 0x31, 0x30, 0x38, 0x7d, 0x2c, 0x22, 0x41, 0x22, 0x3a, 0x32, 0x30, 0x34, 0x2c, 0x22, 0x42, 0x22, 0x3a, 0x33, 0x30, 0x7d}, &input); err != nil {
		panic(err)
	}
	output := input.Call()
	bytes, err := json.Marshal(output)
	if err != nil {
		panic(err)
	}
	fmt.Println(base64.StdEncoding.EncodeToString(bytes))
}
